pub fn frequency_array(arr: &[i32]) -> Vec<i32> {
    let mut array: Vec<i32> = vec![0; 100];

    let arr_usize: Vec<usize> = arr.iter().map(|&i| i.try_into().unwrap()).collect();

    for &i in arr_usize.iter() {
        array[i] += 1;
    }

    array
}

